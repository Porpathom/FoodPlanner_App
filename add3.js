async function adddiabetesMenus() {
  const batch = db.batch();
  let menuCount = 0;
  
  // เพิ่มเมนูอาหารเช้า
  for (const menu of diabetesMenus.breakfast) {
    const menuRef = db.collection('foodMenus').doc();
    batch.set(menuRef, {
      ...menu,
      mealType: "breakfast",
      suitableFor: {
        healthy: false,
        diabetes: true,
        highBloodPressure: false,
        heartDisease: false
      },
      createdAt: admin.firestore.FieldValue.serverTimestamp(),
      updatedAt: admin.firestore.FieldValue.serverTimestamp()
    });
    menuCount++;
  }
  
  // เพิ่มเมนูอาหารกลางวัน
  for (const menu of diabetesMenus.lunch) {
    const menuRef = db.collection('foodMenus').doc();
    batch.set(menuRef, {
      ...menu,
      mealType: "lunch",
      suitableFor: {
        healthy: false,
        diabetes: true,
        highBloodPressure: false,
        heartDisease: false
      },
      createdAt: admin.firestore.FieldValue.serverTimestamp(),
      updatedAt: admin.firestore.FieldValue.serverTimestamp()
    });
    menuCount++;
  }
  
  // เพิ่มเมนูอาหารเย็น
  for (const menu of diabetesMenus.dinner) {
    const menuRef = db.collection('foodMenus').doc();
    batch.set(menuRef, {
      ...menu,
      mealType: "dinner",
      suitableFor: {
        healthy: false,
        diabetes: true,
        highBloodPressure: false,
        heartDisease: false
      },
      createdAt: admin.firestore.FieldValue.serverTimestamp(),
      updatedAt: admin.firestore.FieldValue.serverTimestamp()
    });
    menuCount++;
  }
  
  // ทำการบันทึกข้อมูลทั้งหมด
  await batch.commit();
  console.log(`เพิ่มเมนูอาหารสำหรับผู้ป่วยโรคเบาหวานสำเร็จ จำนวน ${menuCount} เมนู`);
}

